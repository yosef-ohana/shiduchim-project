package com.example.myproject.model;

/**
 * קטגוריות פעולה עבור UserAction.
 * מסייע בסינון, סטטיסטיקות, למידת מכונה וניתוח התנהגות.
 */
public enum UserActionCategory {

    // ==========================
    // 🔵 1. אינטראקציות בסיסיות בין משתמשים
    // ==========================

    SOCIAL,
    // כל האינטראקציות הישירות הפשוטות: LIKE, DISLIKE, FREEZE, UNFREEZE, VIEW, LIKE_BACK
    // מה שמשפיע על הרשימות: "אהבתי", "לא מעוניין", "מוקפא", "צפיתי"

    // ==========================
    // 🔵 2. התאמות (Match)
    // ==========================

    MATCH,
    // כל מה שקשור למאצ'ים:
    // APPROVE_MATCH, CANCEL_MATCH, BLOCK, UNBLOCK, MATCH_CONFIRMED, MATCH_MUTUAL
    // טוב לסטטיסטיקות: כמה התאמות נוצרו, כמה נסגרו, כמה נחסמו וכו'

    // ==========================
    // 🔵 3. צ'אט / תקשורת
    // ==========================

    CHAT,
    // כל מה שקשור לפתיחת/ניהול צ'אט:
    // SEND_MESSAGE, REQUEST_CHAT, APPROVE_CHAT, DECLINE_CHAT, FIRST_MESSAGE_SENT
    // כולל ההודעה הראשונה היחידה (first message), ואישורים/דחיות של שיחה

    // ==========================
    // 🔵 4. חתונות / אירועים
    // ==========================

    EVENT,
    // פעולות ברמת האירוע:
    // ENTER_WEDDING, EXIT_WEDDING, VIEW_WEDDING_MEMBER
    // יכול לשמש לסטטיסטיקות: כמה נכנסו, מי יצא, מי מסתכל על מי בתוך חתונה

    // ==========================
    // 🔵 5. מאגר כללי / גישה גלובלית
    // ==========================

    GLOBAL_POOL,
    // כל מה שקשור לבקשות גלובל:
    // REQUEST_GLOBAL_ACCESS, APPROVE_GLOBAL_ACCESS, ENTER_GLOBAL_POOL, EXIT_GLOBAL_POOL
    // מאפשר לנו לעקוב מי ביקש, מי אושר, מי יצא וכו'

    // ==========================
    // 🔵 6. פרופיל וחשבון
    // ==========================

    PROFILE,
    // פעולות על הפרופיל:
    // PROFILE_UPDATED, PROFILE_BASIC_COMPLETED, PROFILE_FULL_COMPLETED,
    // PROFILE_PHOTO_ADDED, PROFILE_PHOTO_DELETED
    // מעולה לבדוק מי השלים פרופיל, מי עדכן תמונה, מי עוד תקוע על בסיסי בלבד

    // ==========================
    // 🔵 7. מערכת / אדמין
    // ==========================

    SYSTEM,
    // פעולות שמגיעות מהמערכת/אדמין:
    // EVENT_CREATED, EVENT_UPDATED, EVENT_DELETED, SYSTEM_BROADCAST
    // מאפשר להבדיל בין פעולת משתמש לבין פעולת מנהל/מערכת

    // ==========================
    // 🔵 8. אנליטיקה / AI
    // ==========================

    ANALYTICS,
    // שימושים עתידיים:
    // זיהוי דפוסי שימוש, אנומליות, ניקוד AI התנהגותי, בדיקות A/B וכו'
    // טוב כקטגוריה נפרדת כדי לא לערבב עם פעולה "רגילה" של יוזר

    // ✔ ברירת מחדל אם לא הצלחנו לסווג (עדיף לא להשתמש בפועל, אלא רק fallback)
    UNKNOWN


}