package com.example.myproject.model;

/**
 * סוגי הפעולות האפשריות במערכת, עבור UserAction.
 * כל ActionType + Category מאפשרים לנו:
 * - סטטיסטיקות
 * - בניית הרשימות (לייקים, דיסלייקים, קפואים, מחכים לי)
 * - ניתוח התנהגות ו-AI בעתיד
 */
public enum UserActionType {

    // ==========================
    // 🔵 SOCIAL – אינטראקציות בסיסיות
    // ==========================

    LIKE,               // שמתי לייק למשתמש אחר
    LIKE_BACK,          // החזרתי לייק למישהו שכבר שם עליי לייק (לייק חוזר)
    DISLIKE,            // סימנתי "לא מעוניין" על משתמש
    FREEZE,             // העברתי משתמש לרשימת "מוקפאים" (לשמור להמשך)
    UNFREEZE,           // הוצאתי משתמש מרשימת המוקפאים
    VIEW,               // צפייה בפרופיל (לסטטיסטיקות / AI)

    // ==========================
    // 🔵 MATCH – ניהול התאמות
    // ==========================

    APPROVE_MATCH,      // אחד הצדדים מאשר התאמה/היכרות
    CANCEL_MATCH,       // ביטול התאמה (לא רוצה להמשיך)
    MATCH_CONFIRMED,    // נוצרה התאמה (לפני הדדיות מלאה)
    MATCH_MUTUAL,       // שני הצדדים אישרו → התאמה הדדית מלאה
    BLOCK,              // חסימת משתמש (לא רוצה לראות/לדבר)
    UNBLOCK,            // ביטול חסימה

    // ==========================
    // 🔵 CHAT – הודעות וצ'אט
    // ==========================

    SEND_MESSAGE,       // נשלחה הודעה בצ'אט קיים (יש כבר Match/Chat Approved)
    REQUEST_CHAT,       // בקשה לפתיחת צ'אט (אם נרצה להפריד לוגית)
    APPROVE_CHAT,       // הצד השני אישר פתיחת צ'אט
    DECLINE_CHAT,       // הצד השני דחה את פתיחת הצ'אט
    FIRST_MESSAGE_SENT, // ההודעה הראשונית היחידה שמותר לשלוח למישהו (לפני צ'אט מלא)

    // ==========================
    // 🔵 EVENT – התנהגות באירוע / חתונה
    // ==========================

    ENTER_WEDDING,      // משתמש נכנס לחתונה (סריקת QR / כניסה למצב חתונה)
    EXIT_WEDDING,       // יציאה ממצב חתונה
    VIEW_WEDDING_MEMBER,// צפייה בפרופיל של משתתף אחר בחתונה

    // ==========================
    // 🔵 GLOBAL POOL – מאגר כללי
    // ==========================

    REQUEST_GLOBAL_ACCESS,   // המשתמש ביקש להצטרף למאגר הכללי
    APPROVE_GLOBAL_ACCESS,   // מנהל/בעל אירוע אישר
    DECLINE_GLOBAL_ACCESS,   // מנהל דחה בקשה למאגר הכללי
    ENTER_GLOBAL_POOL,       // המשתמש נכנס בפועל למאגר הכללי (inGlobalPool=true)
    EXIT_GLOBAL_POOL,        // יציאה ממאגר גלובלי (אם תהיה לוגיקה כזאת בעתיד)

    // ==========================
    // 🔵 PROFILE – פרופיל וחשבון
    // ==========================

    PROFILE_UPDATED,         // עדכון כללי של הפרופיל
    PROFILE_BASIC_COMPLETED, // סיום פרופיל בסיסי
    PROFILE_FULL_COMPLETED,  // סיום פרופיל מלא (הדרגה שמאפשרת מאגר כללי)
    PROFILE_PHOTO_ADDED,     // הוספת תמונה חדשה
    PROFILE_PHOTO_DELETED,   // מחיקת תמונה

    // ==========================
    // 🔵 SYSTEM – פעולות מערכת / אדמין
    // ==========================

    EVENT_CREATED,           // נוצרה חתונה חדשה במערכת
    EVENT_UPDATED,           // פרטי חתונה עודכנו
    EVENT_DELETED,           // חתונה נמחקה/נסגרה
    SYSTEM_BROADCAST,        // הודעת מערכת רחבה (שידור לכולם / לקבוצה גדולה)

    // ==========================
    // 🔵 AI / ANALYTICS – עתידי
    // ==========================

    AI_SCORE_CALCULATED,     // חושב ניקוד התאמה/התנהגות על ידי AI
    AI_BEHAVIOR_ANOMALY,     // זוהתה אנומליה / דפוס מוזר (לצורך ניטור עתידי)

    // 🔸 ברירת מחדל – אם לא הצלחנו לסווג פעולה (עדיף לא להשתמש באופן רגיל)
    UNKNOWN
}